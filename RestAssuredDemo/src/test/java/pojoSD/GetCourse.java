package pojoSD;

public class GetCourse {
	
	private static String url;
	private static String services;
	private static String expertise;
	private static String courses;
	private static String instructor;
	private static String linkedin;
	
	

	public static String getUrl() {
		return url;
	}
	public static void setUrl(String url) {
		GetCourse.url = url;
	}
	public static String getServices() {
		return services;
	}
	public static void setServices(String services) {
		GetCourse.services = services;
	}
	public static String getExpertise() {
		return expertise;
	}
	public static void setExpertise(String expertise) {
		GetCourse.expertise = expertise;
	}
	public static String getCourses() {
		return courses;
	}
	public static void setCourses(String courses) {
		GetCourse.courses = courses;
	}
	public static String getInstructor() {
		return instructor;
	}
	public static void setInstructor(String instructor) {
		GetCourse.instructor = instructor;
	}
	public static String getLinkedin() {
		return linkedin;
	}
	public static void setLinkedin(String linkedin) {
		GetCourse.linkedin = linkedin;
	}
	
}
